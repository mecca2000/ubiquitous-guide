from docx import Document
from docx.shared import Pt, Cm
from docx.enum.text import WD_ALIGN_PARAGRAPH
from docx.oxml.ns import qn

def set_paragraph_style(paragraph, font_size=12, bold=False, align=None, line_spacing=1.5):
    """设置段落样式"""
    for run in paragraph.runs:
        run.font.size = Pt(font_size)
        run.font.bold = bold
        run.font.name = '宋体'
        run._element.rPr.rFonts.set(qn('w:eastAsia'), '宋体')
    if align:
        paragraph.alignment = align
    paragraph.paragraph_format.line_spacing = line_spacing

def create_title_page(doc, title, student_name, major, class_name, teacher, date):
    """创建封面"""
    # 添加空行
    for _ in range(5):
        doc.add_paragraph()
    
    # 论文标题
    title_para = doc.add_paragraph(title)
    title_para.alignment = WD_ALIGN_PARAGRAPH.CENTER
    for run in title_para.runs:
        run.font.size = Pt(22)
        run.font.bold = True
        run.font.name = '黑体'
        run._element.rPr.rFonts.set(qn('w:eastAsia'), '黑体')
    
    # 空行
    for _ in range(3):
        doc.add_paragraph()
    
    # 学生信息
    info_lines = [
        f"学生姓名：{student_name}",
        f"专    业：{major}",
        f"班    级：{class_name}",
        f"指导教师：{teacher}",
        f"完成日期：{date}"
    ]
    
    for line in info_lines:
        para = doc.add_paragraph(line)
        para.alignment = WD_ALIGN_PARAGRAPH.CENTER
        for run in para.runs:
            run.font.size = Pt(16)
            run.font.name = '楷体'
            run._element.rPr.rFonts.set(qn('w:eastAsia'), '楷体')
    
    return doc

def create_abstract(doc, title, abstract_text, keywords):
    """创建摘要页"""
    # 摘要标题
    abstract_title = doc.add_paragraph("摘  要")
    abstract_title.alignment = WD_ALIGN_PARAGRAPH.CENTER
    for run in abstract_title.runs:
        run.font.size = Pt(16)
        run.font.bold = True
        run.font.name = '黑体'
        run._element.rPr.rFonts.set(qn('w:eastAsia'), '黑体')
    
    doc.add_paragraph()
    
    # 摘要内容
    abstract_para = doc.add_paragraph(abstract_text)
    set_paragraph_style(abstract_para, font_size=12, line_spacing=1.5)
    
    doc.add_paragraph()
    
    # 关键词
    kw_para = doc.add_paragraph(f"关键词：{keywords}")
    set_paragraph_style(kw_para, font_size=12, bold=True)
    
    # 分页
    doc.add_page_break()
    
    return doc

def create_toc(doc):
    """创建目录"""
    toc_title = doc.add_paragraph("目  录")
    toc_title.alignment = WD_ALIGN_PARAGRAPH.CENTER
    for run in toc_title.runs:
        run.font.size = Pt(16)
        run.font.bold = True
        run.font.name = '黑体'
        run._element.rPr.rFonts.set(qn('w:eastAsia'), '黑体')
    
    doc.add_paragraph()
    doc.add_paragraph("（此处为目录，实际使用时请手动生成）")
    
    doc.add_page_break()
    
    return doc

